	<script type="text/javascript">
		
		/***********************************\
				   Setup buttons
		\***********************************/
		var numItems = 8; // total number of items
		for (var i=0; i<numItems; i++)
		{
			// set ID of item
			$('#about-scroll-item' + i).attr({itemID: i});
			
			// Bind click events
			$('#about-scroll-item' + i).click(function() {
				var thisID = ($(this).attr("itemID"));
				
				displayContent(thisID);
			});		
			
			// Bind click events to all back buttons
			$('#back-button-' + i).click(function() {
				displayContentHome();
			});		
			
		}
		
		function displayContentHome()
		{
			// fade in home item
			$('#content-home').css("visibility", "visible");

			// fade out all items
			for (var i=0; i<numItems; i++)
			{
			
				$('#content-' + i).css("visibility", "hidden");
				$('#content-image-' + i).css("visibility", "hidden");
				
				$('#about-scroll-item' + i).css("background-image", "url(images/about/item-thumb-"+i+".png)"); // switch thumbnail to unselected
			}	
		}
		
		function displayContent(id)
		{
			// fade out home item
			$('#content-home').css("visibility", "hidden");
			
			// fade out all items except selected
			for (var i=0; i<numItems; i++)
			{
				if (i == id)
				{
				
					$('#content-' + i).css("visibility", "visible");
					$('#content-image-' + i).css("visibility", "visible");
					
					$('#about-scroll-item' + i).css("background-image", "url(images/about/item-thumb-"+i+"-on.png)"); // switch thumbnail to selected
				}
				else
				{
				
					$('#content-' + i).css("visibility", "hidden")
					$('#content-image-' + i).css("visibility", "hidden")
					
					$('#about-scroll-item' + i).css("background-image", "url(images/about/item-thumb-"+i+".png)"); // switch thumbnail to unselected
				}
			}
		}
		
		/***********************************\
				  Setup scrolling
		\***********************************/
		// Init vars
		var currentPos = 0; // current position of scroller (which item is at the left)
		
		var itemWidth = 141; // width of each item - used to animate to correct one.
		var minPos = -2; // minimu position - basically Math.round(numItems - visibleItems)
		
		// Set up left and right buttons
		$('#about-scroll-button-left').click(function() {
			scrollRight();
			manageButtons();
		});
		$('#about-scroll-button-right').click(function() {
			scrollLeft();
			manageButtons();
		});		
		
		function scrollLeft()
		{
			currentPos--;
			if (currentPos <= minPos)
			{
				currentPos = minPos;
			}
			newLeft = (currentPos*itemWidth) + "px";
			$('#scrollList').animate({left:newLeft},500);
		}
		function scrollRight()
		{
			currentPos++;
			if (currentPos > 0)
			{
				currentPos = 0;
			}
			newLeft = (currentPos*itemWidth) + "px";
			$('#scrollList').animate({left:newLeft},500);
		}
		
		/**************************************************************\
		Fade left/right buttons in and out depending on scroll position
		\**************************************************************/
		function manageButtons()
		{
			if (currentPos >= 0)
			{
				$('#about-scroll-button-left').fadeOut();
			}
			else
			{
				$('#about-scroll-button-left').fadeIn();
			}

			if (currentPos <= minPos)
			{
				$('#about-scroll-button-right').fadeOut();
			}
			else
			{
				$('#about-scroll-button-right').fadeIn();
			}			
		}
		
		manageButtons();
		
	</script> 
 
